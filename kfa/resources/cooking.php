</div>
<div class="ltgreyboxmainR">

			<!-- #BeginEditable "text" -->
<p align="center"><strong>Cooking 101 </strong></p>
<p>When your child is diagnosed with severe food allergies, you need to learn new ways to approach common, everyday situations. You need to learn how to determine which foods are &quot;safe&quot; for your child and which are not. You need to learn how to handle social situations, school, travel, and more. And if you're like many parents, you also need to learn how to cook. </p>
<p>Before your child was put on a &quot;strict avoidance&quot; diet you may have done little, if any cooking. These days many busy parents have practically given up cooking altogether and rely instead on sit-down restaurants, fast-food, take-out, ready-to-eat meals from the supermarket, and frozen dinners. Unfortunately, many of these options may either be unsafe or very limited for your food-allergic child. Cooking &quot;from scratch&quot; is the safest way to ensure that your child eats a healthy, balanced, and <em>safe </em> diet. </p>
<p>So where do you start? Well, learning to cook is as simple as 1-2-3: </p>
<ol>
  <li>Find appropriate recipes </li>
  <li>Understand how to measure ingredients </li>
  <li>Learn specific cooking techniques </li>
</ol>
<p>Note: if your kitchen isn't actually set up for cooking, you'll want to see our article on Equipment Essentials . </p>
<p><strong>Finding Recipes </strong></p>
<p>Our Safe Eats database is the perfect place to start your search for recipes that will suit your child's new diet. You can search the database based on your child's specific dietary needs or you can browse by category. There are also many excellent special-diet cookbooks on the market today; our Book Shop gives a run-down on some of the popular ones. </p>
<p><strong>Measuring Ingredients </strong></p>
<p>There is a right way and a wrong way to measure ingredients. For example, liquid ingredients are measured using different tools than dry ingredients, and some ingredients are meant to be loosely placed in the measuring cup, while others should be packed in. Here are some online resources for learning the ins and outs of measuring: </p>
<ul>
  <li>About.com's Guide for Busy Cooks: <em>How to Measure </em></li>
</ul>
<p><a href="http://busycooks.about.com/od/howtocook/a/howtomeasure.htm">http://busycooks.about.com/od/howtocook/a/howtomeasure.htm </a></p>
<ul>
  <li>Fabulous Foods' Cooking School: <em>All About Measuring </em></li>
</ul>
<p><a href="http://www.fabulousfoods.com/cgi-bin/schoollinks/jump.cgi?ID=10">http://www.fabulousfoods.com/cgi-bin/schoollinks/jump.cgi?ID=10 </a></p>
<p>Keep in mind that many recipes use abbreviations when specifying measurements: </p>
<p>tsp = teaspoons </p>
<p>T or Tbsp = tablespoons </p>
<p>c = cup </p>
<p>oz = ounces </p>
<p>Need help converting measurements from one measuring system to another? We've got you covered with our resource on Conversion Calculators and Charts .</p>
<p><strong>Learning Cooking Techniques </strong></p>
<p>A recipe is simply a set of instructions for cooking a particular dish. In order to follow these instructions you'll need an understanding of basic cooking techniques. Don't have time to sign up for a cooking course or sit through hours of cooking shows on TV? Don't worry. We've scoured the internet to bring you a host of resources that will get you cooking like a pro in no time. Check out these links to helpful learn-to-cook videos and articles. </p>
<ul>
  <li><strong>Food Network's Cooking Techniques videos </strong></li>
</ul>
<p>View step-by-step instruction for such common techniques as saut&eacute;ing, deep-frying, deglazing, seeding, folding batter, and braising. </p>
<p><a href="http://www.foodnetwork.com/food/ck_dm_cooking_techniques">http://www.foodnetwork.com/food/ck_dm_cooking_techniques </a></p>
<p>&#149;&nbsp; <strong>AZ Central.com's Cooking Demos videos </strong></p>
<p>Step-by-step videos show you everything from basics like &quot;How to Deep-Fry,&quot; &quot;Cutting a Chicken,&quot; and &quot;Knife Skills&quot; to more advanced &quot;Butchering Pork Loin,&quot; and &quot;Making Chocolate Bowls,&quot; and lots of stuff in between. (Note: each video is preceded by a brief advertisement.) </p>
<p><a href="http://www.azcentral.com/home/recipes/">http://www.azcentral.com/home/recipes/ </a></p>
<ul>
  <li><strong>Wegmans' Cooking Techniques </strong></li>
</ul>
<p>Sharpen your culinary skills with this combination of videos and illustrated step-by-step instructions for all of the most common cooking techniques, including braising, steaming, stir-frying, grilling, poaching, cooking pasta, and more. </p>
<p><a href="http://www.wegmans.com/greatMeals/cookingTechniques/">http://www.wegmans.com/greatMeals/cookingTechniques/ </a></p>
<p>&#149;&nbsp; <strong>Epicurious' Technique and How-To Videos </strong></p>
<p>This extensive library of cooking videos includes many topics not seen elsewhere. In the baking category, selections include &quot;Preparing Cake Pans,&quot; &quot;Creaming, Beating, and Folding,&quot; &quot;Frosting a Cake,&quot; &quot;Rolling Out Cookie Dough,&quot; and &quot;Pie Making.&quot; If you're goal is to get dinner on the table, you may be interested in &quot;Testing Meat Doneness,&quot; &quot;Tying a Roast,&quot; &quot;Forming Meatballs or Hamburger Patties,&quot; or &quot;How to Make Gravy.&quot; </p>
<p><a href="http://www.epicurious.com/cooking/how_to/video/">http://www.epicurious.com/cooking/how_to/video/ </a></p>
<ul>
  <li><strong>All Recipes' How-To Videos </strong></li>
</ul>
<p>Although most of the videos in this extensive library are illustrations of particular recipes, there's a lot of good &quot;technique&quot; information to be found here, too. Selections include &quot;Cooking in Parchment Paper,&quot; &quot;How to Cook an Artichoke,&quot; &quot;How to Poach Fish,&quot; &quot;Melting Chocolate,&quot; &quot;Pre-Baking Pie Crusts,&quot; &quot;Roasting Garlic,&quot; and more. </p>
<p><a href="http://allrecipes.com/Info/Cooking-School/How-To-Videos/ViewAll.aspx">http://allrecipes.com/Info/Cooking-School/How-To-Videos/ViewAll.aspx</a></p>
<ul>
  <li><strong>Fabulous Foods Cooking School </strong></li>
</ul>
<p>These step-by-step articles (many of which include photo illustrations) cover a range of subjects, including &quot;Cookware Primer,&quot; &quot;Cake &amp; Cookie Decorating,&quot; &quot;How to Bone a Chicken Breast,&quot; &quot;How to Grill a Great Steak,&quot; &quot;How to Make a Perfect Roux,&quot; and more. </p>
<p><a href="http://www.fabulousfoods.com/school/">http://www.fabulousfoods.com/school/</a></p>
<p>&#149;&nbsp; <strong>Chef 2 Chef Culinary Portal's Online Courses </strong></p>
<p>Free online courses to help you &quot;learn to cook like a chef.&quot; Explanations and recipes covering sauce making, seasoning and flavoring (learn when to season and when to flavor), grilling and smoking, and instructions on French, Italian, Chinese and Cajun cuisines. Although these courses do not include illustrations or videos, they are quite informative. </p>
<p><a href="http://recipes.chef2chef.net/learn-to-cook/">http://recipes.chef2chef.net/learn-to-cook/ </a></p>


